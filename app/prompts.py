def build_user_prompt(area: AreaSpec, *, subject: str, keywords: str,
                      tone: str, length_hint: str, n_variations: int = 1) -> str:
    """교사 입력을 사용자 메시지로 조립."""
    parts: list[str] = []
    if area.subject_field and subject.strip():
        parts.append(f"과목: {subject.strip()}")
    if tone.strip():
        parts.append(f"문체/톤 요청: {tone.strip()}")
    if length_hint.strip():
        parts.append(f"분량: {length_hint.strip()}")

    label = "다듬을 초안" if area.key == "polish" else "키워드/관찰 내용"
    parts.append(f"{label}:\n{keywords.strip()}")
    parts.append("위 키워드에 나온 용어·고유명사(과목 전문 용어 포함)는 "
                 "철자를 바꾸지 말고 그대로 정확히 사용한다.")

    if n_variations > 1:
        # '핵심별 표현 후보(5개)'를 뽑아 어순·표현 조합으로 변형을 만든다.
        # (작은 모델은 완성 변형 N개 직접 생성 시 반복·붕괴 → 조합 방식이 안정적)
        parts.append(
            "\n위 키워드의 핵심 내용을 항목별로 정리한다. 규칙:\n"
            "- 한 줄에 하나의 핵심 내용을 쓴다.\n"
            "- 각 핵심 내용은 그것을 표현하는 자연스러운 명사형 문장 5가지를, 의미는 같되 "
            "어휘·표현·길이를 다르게 하여 ' / ' 로 구분해 나열한다.\n"
            "- 각 표현은 한 문장으로 쓰고, 접속어로 시작하지 않는다.\n"
            "- 주어진 키워드의 사실만 쓰고, 새로운 정보를 지어내지 않는다.\n"
            "예시 형식:\n"
            "맡은 역할을 성실히 수행함 / 맡은 일을 끝까지 책임감 있게 해냄 / 주어진 역할을 꾸준히 완수함 / "
            "자신이 맡은 일에 최선을 다하는 모습을 보임 / 책임을 끝까지 다하는 자세를 지님\n"
            "친구들을 배려하는 태도를 보임 / 또래를 따뜻하게 챙기는 모습을 드러냄 / 친구를 먼저 돕는 자세를 지님 / "
            "어려운 친구를 살피는 마음을 지님 / 서로 돕는 분위기를 이끄는 모습을 보임"
        )
    return "\n".join(parts)
